    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String stuID = sID.getText();
        String bookID = bID.getText();
        String borDate = bDate.getText();
        String retDate = rDate.getText();
        
        File file = new File("Data/Borrow.txt");
        try {
            if (file.exists() == false) {
                file.createNewFile();
            }
            PrintWriter out = new PrintWriter(new FileWriter(file, true));

            out.append(stuID + "\r");
            out.append(bookID + "\r");
            out.append(borDate + "\r");
            out.append(retDate + "\r\r\r");
            out.close();

            JOptionPane.showMessageDialog(null, "Book is given!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

