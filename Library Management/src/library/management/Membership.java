    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String ID = id.getText();
        String Name = name.getText();
        String Dept = dept.getSelectedItem().toString();
        String Num = num.getText();
        String Email = email.getText();

        //File file = new File("Data/" + ID + ".txt");
        File file = new File("Data/members.txt");
        try {
            if (file.exists() == false) {
                file.createNewFile();
            }
            PrintWriter out = new PrintWriter(new FileWriter(file, true));

            out.append(ID + "\r");
            out.append(Name + "\r");
            out.append(Dept + "\r");
            out.append(Num + "\r");
            out.append(Email + "\r\r");
            out.close();

            JOptionPane.showMessageDialog(null, "Membership Updated!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Invalid file creation");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

