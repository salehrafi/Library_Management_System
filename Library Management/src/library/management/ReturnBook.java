    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String filePath = "Data/Borrow.txt";
        String rid = searchbar.getText();

        try {
            File file = new File(filePath);
            FileReader fileReader = new FileReader(file);
            BufferedReader bufferedReader = new BufferedReader(fileReader);
            StringBuilder fileContent = new StringBuilder();

            String line;
            int ltr = 3;

            while ((line = bufferedReader.readLine()) != null) {
                if (line.contains(rid)) {
                    while (ltr > 0 && bufferedReader.readLine() != null) {
                        ltr--;
                    }
                } else {
                    fileContent.append(line).append("\n");
                }
            }

            bufferedReader.close();
            fileReader.close();

            FileWriter fileWriter = new FileWriter(file);
            BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);

            bufferedWriter.write(fileContent.toString());

            bufferedWriter.close();
            fileWriter.close();

            JOptionPane.showMessageDialog(null, "Returned!");
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println(e);
            //JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed

        String sID, bID, bDate, rDate;

        String id = searchbar.getText() + ".txt";
        try {
            File file = new File("Data/Borrow.txt");
            Scanner scanner = new Scanner(file);
            boolean found = false;

            while (scanner.hasNext()) {
                String ID = scanner.nextLine();
                if (id.equals(ID + ".txt")) {
                    found = true;

                    sID = scanner.nextLine();
                    bID = scanner.nextLine();
                    bDate = scanner.nextLine();
                    rDate = scanner.nextLine();

                    l1.setText(sID);
                    l2.setText(bID);
                    l3.setText(bDate);

                    break;
                }
            }
            scanner.close();

            if (!found) {
                JOptionPane.showMessageDialog(null, "Not found!");
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error reading data!");
        }


    }//GEN-LAST:event_searchbtnActionPerformed

